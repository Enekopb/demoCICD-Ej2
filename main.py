"""
Este módulo realiza operaciones de suma.

Puedes proporcionar dos números y obtener su suma como resultado.
"""

def suma(a, b):
    """
    Suma dos números.

    Args:
        a (int): Primer número.
        b (int): Segundo número.

    Returns:
        int: La suma de los dos números.
    """
    return a + b

A = 2
B = 3
print("La suma de 2 y 3 es:", suma(A, B))
